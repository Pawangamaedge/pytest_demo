class Algebra:
    def square(x):
        return x**2

    def cube(x):
        return x**3
    
    def add(z,a):
        return z+a
    
    def sub(a,b):
        return a-b

class Geometry:
    def is_triangle(a, b, c):
        return a + b + c == 180

    def is_quadrilateral(w, x, y, z):
        return w + x + y + z == 360